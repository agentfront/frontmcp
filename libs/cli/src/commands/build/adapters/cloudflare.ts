import type { CloudflareDeployment } from '../../../config/frontmcp-config.types';
import type { AdapterTemplate } from '../types';

/**
 * Cloudflare Workers adapter - edge deployment on Cloudflare.
 * Compiles to CommonJS and adapts the Express app to Cloudflare's fetch API.
 *
 * @see https://developers.cloudflare.com/workers/
 */
export const cloudflareAdapter: AdapterTemplate = {
  // ES Module format — modern Cloudflare Workers (and `nodejs_compat`) require
  // the Module Worker shape (`export default { fetch }`). The legacy CommonJS
  // `module.exports` is interpreted as a Service Worker, where `nodejs_compat`
  // cannot externalize Node builtins and the build fails.
  moduleFormat: 'esnext',

  // Emitted as `serverless-setup.js` and imported FIRST by the entry, so the
  // env flags are set before the user module's `@FrontMcp` decorator evaluates
  // (ESM import evaluation is ordered, and the decorator reads these at import
  // time). FRONTMCP_WORKER selects FrontMCP's Web-standard fetch handler.
  getSetupTemplate: () => `// Auto-generated — sets env before the @FrontMcp decorator runs.
process.env.FRONTMCP_SERVERLESS = '1';
process.env.FRONTMCP_DEPLOYMENT_MODE = 'serverless';
process.env.FRONTMCP_WORKER = '1';
`,

  getEntryTemplate: (mainModulePath: string) => `// Auto-generated Cloudflare Workers entry point (ES Module / Module Worker).
// Generated by: frontmcp build --target cloudflare
//
// The Worker receives the native Web Request and the SDK routes it straight
// into the MCP WebStandard transport (Request/Response). No Express, no Node
// req/res shim.
import './serverless-setup.js';
import '${mainModulePath}';
import { getServerlessHandlerAsync } from '@frontmcp/sdk';

let handlerPromise = null;

export default {
  async fetch(request, env, ctx) {
    if (!handlerPromise) {
      handlerPromise = getServerlessHandlerAsync();
    }
    // In worker mode the stored handler is a Web fetch handler:
    // (Request) => Promise<Response>.
    const handler = await handlerPromise;
    return handler(request);
  },
};
`,

  // #375 — fail the build when the user's @FrontMcp config references
  // Node-only storage providers that can't run on Workers.
  //
  // Round 1 only inspected the runtime-evaluated config object. The reporter's
  // failure case was `sqlite: process.env.REDIS_HOST ? {…} : { sqlite: {…} }` —
  // a ternary that may evaluate to `undefined` at decorator-load time (so the
  // runtime check sees nothing) but still ships the Node-only branch in the
  // bundled worker. Round 2 also inspects `info.keysSeenInSource`, which is
  // collected by walking the @FrontMcp({...}) source expression — that captures
  // the property name regardless of whether its value is a literal, a ternary,
  // or a function call. If `sqlite`/`redis` appear at all, fail loud and tell
  // the user how to gate them at the source level.
  validate: (decoratorConfig, info) => {
    const errors: string[] = [];

    const sqliteIsLiteral =
      decoratorConfig?.['sqlite'] !== undefined &&
      typeof decoratorConfig['sqlite'] === 'object' &&
      decoratorConfig['sqlite'] !== null;
    const redisIsLiteral =
      decoratorConfig?.['redis'] !== undefined &&
      typeof decoratorConfig['redis'] === 'object' &&
      decoratorConfig['redis'] !== null;
    const sqliteSeen = sqliteIsLiteral || !!info?.keysSeenInSource?.includes('sqlite');
    const redisSeen = redisIsLiteral || !!info?.keysSeenInSource?.includes('redis');

    if (sqliteSeen) {
      errors.push(
        'sqlite storage is not supported on --target cloudflare (no fs / native modules on Workers). ' +
          'Even an env-gated `sqlite: process.env.X ? {...} : undefined` still ships the Node-only ' +
          'branch in the worker bundle. Use Cloudflare KV / Durable Objects, or move the sqlite ' +
          'config behind a build-time `define` so the bundler can dead-code-eliminate it.',
      );
    }
    if (redisSeen) {
      errors.push(
        'ioredis-style `redis` storage is not supported on --target cloudflare (no Node net). ' +
          'Even an env-gated `redis: process.env.X ? {...} : undefined` still ships the Node-only ' +
          'branch in the worker bundle. Use Vercel KV / Upstash Redis (HTTP), or move the redis ' +
          'config behind a build-time `define` so the bundler can dead-code-eliminate it.',
      );
    }
    if (errors.length) {
      throw new Error(
        `[--target cloudflare] config incompatible with Cloudflare Workers:\n  - ${errors.join('\n  - ')}`,
      );
    }
  },

  // #374 — always write wrangler.toml from the build output. Skipping when
  // the file already exists left users with a wrangler.toml that pointed at
  // dist/index.js while the build emitted dist/cloudflare/index.js, and
  // wrangler deploy silently failed.
  alwaysWriteConfig: true,

  // #374 round-2 — merge `frontmcp.config.deployments[].wrangler.{name,
  // compatibilityDate, compatibilityFlags}` into the rendered TOML so values
  // declared in the user's config actually reach `wrangler deploy`. Defaults
  // preserved when the field is absent or no deployment was matched.
  getConfig: (_cwd, deployment) => {
    const wrangler = (deployment as CloudflareDeployment | undefined)?.wrangler ?? {};
    const name = wrangler.name ?? 'frontmcp-worker';
    // The cloudflare entry (getEntryTemplate) is CommonJS and `require()`s
    // `@frontmcp/sdk` + Express, which pull in Node builtins (node:*, Buffer,
    // process, streams). On Workers those exist ONLY behind the `nodejs_compat`
    // flag — without it the Worker fails to even load. The flag is therefore
    // non-negotiable for this target; we always emit it and merge in any extra
    // flags the user declared (deduped, `nodejs_compat` guaranteed first).
    const flags = Array.from(new Set(['nodejs_compat', ...(wrangler.compatibilityFlags ?? [])]));
    // `nodejs_compat` only provides the full Node API surface (incl. `require`
    // of builtins) when compatibility_date >= 2024-09-23; default to that so a
    // freshly-built worker boots. Users can still pin an older/newer date.
    const compatibilityDate = wrangler.compatibilityDate ?? '2024-09-23';
    return `name = "${name}"
main = "dist/cloudflare/index.js"
compatibility_date = "${compatibilityDate}"
compatibility_flags = [${flags.map((f) => `"${f}"`).join(', ')}]
`;
  },

  configFileName: 'wrangler.toml',
};
